# Types of operators
# 1.Arithmatic
# 2.Assignment
# 3.Relational
# 4.Logical
# 5.Unary

x = 6
y = 2
# arithmatic operator
print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y)

# assignment

x = 5
x = x + 5

# relational operator

print(x > y)
print(x < y)
print(x == y)
print(x <= y)
print(x >= y)
print(x != y)

# Logical operators

print((x < y) and (x > y))
print((x < y) or (x > y))
